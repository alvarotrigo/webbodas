// Fetch tags from Lucide GitHub (parallel with concurrency limit)
// ═══════════════════════════════════════════════════════════════
async function fetchIconMeta(name, retries = 2) {
  const url = `${LUCIDE_RAW}/${name}.json`;
  for (let attempt = 0; attempt <= retries; attempt++) {
    try {
      const res = await fetch(url);
      if (res.status === 404) return { tags: [], categories: [] };
      if (!res.ok) throw new Error(`HTTP ${res.status}`);
      const data = await res.json();
      return {
        tags: data.tags || [],
        categories: data.categories || []
      };
    } catch (err) {
      if (attempt === retries) {
        console.warn(`   ⚠ Failed to fetch metadata for ${name}: ${err.message}`);
        return { tags: [], categories: [] };
      }
      await new Promise(r => setTimeout(r, 200 * (attempt + 1)));
    }
  }
}

async function fetchAllMeta(names) {
  const CONCURRENCY = 40;
  const results = new Map();
  const queue = [...names];
  let completed = 0;

  async function worker() {
    while (queue.length > 0) {
      const name = queue.shift();
      if (!name) break;
      results.set(name, await fetchIconMeta(name));
      completed++;
      if (completed % 100 === 0) {
        process.stdout.write(`   … fetched metadata: ${completed}/${names.length}\r`);
      }
    }
  }

  const workers = Array.from({ length: CONCURRENCY }, () => worker());
  await Promise.all(workers);
  console.log(`   ✓ Fetched metadata for ${results.size} icons`);
  return results;
}

