function translateTag(tag) {
  const lower = tag.toLowerCase().trim();
  // Try full phrase first
  if (ES._words[lower]) return ES._words[lower];
  // Try word by word
  const words = lower.split(/[\s-]+/);
  const translated = words.map(w => ES._words[w] || w);
  const result = translated.join(' ');
  return result !== lower ? result : null; // null if no translation found
}

